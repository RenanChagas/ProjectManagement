package br.com.management.model;

public enum SortingType {
	LATEST("Latest"),
	NEWEST("Newest"),
	FINISHED("Finished");
	
	String sortingType;

	SortingType(String sortingType){
		this.sortingType = sortingType;
	}
	
	public String getSortingType() {
		return sortingType;
	}

	public void setSortingType(String sortingType) {
		this.sortingType = sortingType;
	}

}
